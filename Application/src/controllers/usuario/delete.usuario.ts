import { pool } from "../../imports";

export const deleteUsuario = async (req: any,res: any) => {
    pool.connect((error, client, release) => {
        if (error) {
        return res.status(500).json({ error: 'Erro ao obter conexÃ£o do banco de dados' });
        }

        
        client.query("DELETE FROM usuario WHERE cpf_usuario = $1", [req.params.cpf_usuario], (queryError, result) => {
        release();

        if (queryError) {
            return res.status(500).json({ error: 'Erro ao executar a consulta' });
        }

        res.json(result.rows);
        });
    });
}